#include <unistd.h>

void	ft_is_negative(int n)
{
	char	buffer;

	if (n < 0)
	{
		buffer = 'N';
		write(1, &buffer, 1);
	}
	else
	{	
		buffer = 'P';
		write(1, &buffer, 1);
	}
}
