int	ft_str_is_printable(char *str)
{
	int	i;
	int	char_as_int;

	i = 0;
	while (str[i] != '\0')
	{
		char_as_int = str[i];
		if (str[i] < 32 || str[i] == 127)
			return (0);
		i++;
	}
	return (1);
}
